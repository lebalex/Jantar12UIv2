    private void jButtonStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonStartActionPerformed
        // TODO add your handling code here:
        if(!start)
        {
            chooser = new JFileChooser(); 
            String dir = LoadData.getMainProp("result_path");
            if(dir==null) dir = ".";
            chooser.setCurrentDirectory(new java.io.File(dir));
            chooser.setDialogTitle("Выбор папки для записи результатов");
            chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
            chooser.setAcceptAllFileFilterUsed(false);
  
            if (chooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) { 
                jTextAreaLog.setText(null);
                jProgressBar1.setValue(0);
                jProgressBar1.setMaximum(new SXMClass(null).getKS(projectName+".SXM")+4);
                String dir2 = chooser.getSelectedFile().toString();
                dir2 = dir2.replace('\\', '/');
                LoadData.saveProp("result_path", dir2);
                if(getAnalogName(dir2+"/"+jTextPrefFileName.getText().trim()))
                {
                    if(JOptionPane.showConfirmDialog(this, "Файлы с таким именем уже существует, заменить файлы?", "Заменить файлы", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
                        runCreateResults = new RunCreateResults(projectName, jTextPrefFileName.getText(), this, chooser.getSelectedFile()+"/");
                        runCreateResults.start();
                    }
                }else
                {
                        runCreateResults = new RunCreateResults(projectName, jTextPrefFileName.getText(), this, chooser.getSelectedFile()+"/");
                        runCreateResults.start();
                }
            }
            else {
                 System.out.println("No Selection ");
            }
        }else
        {
            if(JOptionPane.showConfirmDialog(this, "Остановить формирование результатов?", "Остановить!", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION)
            {
                if(runCreateResults!=null && runCreateResults.isAlive() && !runCreateResults.isInterrupted())
                {
                    runCreateResults.setStop();
                    runCreateResults.interrupt();
                    runCreateResults=null;
                }
            }
        }
        
        //jButtonStart.setEnabled(true);
        //jButtonCancel.setEnabled(true);
    }//GEN-LAST:event_jButtonStartActionPerformed
    private boolean getAnalogName(String fName)
    {
        try{
            File file = new File(fName+"_Sistem.xlsx");
            if(file.exists())
                return true;
            else
                return false;
        }catch(Exception e)
        {
            return true;
        }
    }
